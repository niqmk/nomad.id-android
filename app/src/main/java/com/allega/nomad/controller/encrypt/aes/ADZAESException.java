package com.allega.nomad.controller.encrypt.aes;

public class ADZAESException extends Exception {

    private static final long serialVersionUID = 5863541408038919969L;

    public ADZAESException() {
        super();
    }


    public ADZAESException(String message) {
        super(message);
    }

    public ADZAESException(Throwable cause) {
        super(cause);
    }

    public ADZAESException(String message, Throwable cause) {
        super(message, cause);
    }

}
